export function defaultThemeVars(presetId = 'dark') {
  return { ...(THEME_PRESETS[presetId] || THEME_PRESETS.dark).vars };
}

export function createEmptyCustomTheme(name = 'Моя тема') {
  return {
    id: `custom_${Date.now()}`,
    name,
    vars: defaultThemeVars('dark'),
  };
}
